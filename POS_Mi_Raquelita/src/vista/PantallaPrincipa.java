    private void btnAbrirCajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirCajaActionPerformed
            try {
        // 1. Pedimos el nombre del cajero
            String nombreCajero = javax.swing.JOptionPane.showInputDialog(this, "Nombre del cajero responsable:");
            if (nombreCajero == null || nombreCajero.trim().isEmpty()) return;

            // 2. Pedimos el monto inicial
            String montoInput = javax.swing.JOptionPane.showInputDialog(this, "Efectivo inicial para el turno de " + nombreCajero + ":");
            if (montoInput == null || montoInput.isEmpty()) return;

            baseCaja = Double.parseDouble(montoInput);

            // 3. Guardamos en la Base de Datos
            conexion.ConexionDB con = new conexion.ConexionDB();
            java.sql.Connection conexion = con.establecerConexion();
            
            String sql = "INSERT INTO caja_diaria (base_inicial, fecha_apertura, estado, cajero) VALUES (?, NOW(), 'ABIERTA', ?)";
            java.sql.PreparedStatement pst = conexion.prepareStatement(sql, java.sql.Statement.RETURN_GENERATED_KEYS);
            pst.setDouble(1, baseCaja);
            pst.setString(2, nombreCajero);
            pst.executeUpdate();

            java.sql.ResultSet rs = pst.getGeneratedKeys();
            if (rs.next()) { idTurnoActual = rs.getInt(1); }
            
            conexion.close();
            comprobarEstadoCaja(); 
            javax.swing.JOptionPane.showMessageDialog(this, "¡Turno iniciado por " + nombreCajero + "!");
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAbrirCajaActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
            try {
        conexion.ConexionDB con = new conexion.ConexionDB();
            java.sql.Connection conexion = con.establecerConexion();
            
            // 1. Buscamos ventas SOLO en el rango de este turno
            String sqlVentas = "SELECT SUM(subtotal) AS total FROM ventas WHERE fecha >= CAST(? AS DATETIME)";
            java.sql.PreparedStatement pstVentas = conexion.prepareStatement(sqlVentas);
            pstVentas.setString(1, horaAperturaTurno);
            java.sql.ResultSet rsV = pstVentas.executeQuery();
            
            double totalVentasTurno = 0;
            if (rsV.next()) { totalVentasTurno = rsV.getDouble("total"); }

            double granTotalCaja = baseCaja + totalVentasTurno;
            
            // ---  Buscamos el nombre de quién abrió este turno ---
            String responsable = "No asignado";
            try {
                java.sql.PreparedStatement pstNombre = conexion.prepareStatement("SELECT cajero FROM caja_diaria WHERE id = ?");
                pstNombre.setInt(1, idTurnoActual);
                java.sql.ResultSet rsN = pstNombre.executeQuery();
                if (rsN.next()) {
                    responsable = rsN.getString("cajero");
                }
            } catch (Exception e) {
                System.out.println("Error al buscar nombre: " + e.getMessage());
            }

            String mensaje = "RESUMEN DE ESTE TURNO\n"
                           + "Inicio: " + horaAperturaTurno + "\n"
                           + "Base: $ " + baseCaja + "\n"
                           + "Ventas: $ " + totalVentasTurno + "\n"
                           + "EFECTIVO EN CAJA: $ " + granTotalCaja;

            int opc = javax.swing.JOptionPane.showConfirmDialog(this, mensaje + "\n\n¿Cerrar turno definitivamente e imprimir ticket?", "Cierre de Turno", javax.swing.JOptionPane.YES_NO_OPTION);

            if (opc == javax.swing.JOptionPane.YES_OPTION) {
                // 2. ACTUALIZAMOS LA BASE DE DATOS (Cerramos la caja oficialmente)
                String sqlCierre = "UPDATE caja_diaria SET fecha_cierre = NOW(), estado = 'CERRADA' WHERE id = ?";
                java.sql.PreparedStatement pstC = conexion.prepareStatement(sqlCierre);
                pstC.setInt(1, idTurnoActual);
                pstC.executeUpdate();

                // ---  MAGIA: GENERAMOS EL PDF DEL CIERRE  ---
                try {
                    com.itextpdf.text.Document documento = new com.itextpdf.text.Document();
                    String nombreArchivo = "Cierre_Turno_" + idTurnoActual + ".pdf";
                    com.itextpdf.text.pdf.PdfWriter.getInstance(documento, new java.io.FileOutputStream(nombreArchivo));

                    documento.open();
                    documento.add(new com.itextpdf.text.Paragraph("========================================"));
                    documento.add(new com.itextpdf.text.Paragraph("          CIERRE DE CAJA - MI RAQUELITA         "));
                    documento.add(new com.itextpdf.text.Paragraph("========================================"));
                    documento.add(new com.itextpdf.text.Paragraph("Turno #: " + idTurnoActual));                    
                    documento.add(new com.itextpdf.text.Paragraph("Responsable:      " + responsable));
                    documento.add(new com.itextpdf.text.Paragraph("Apertura: " + horaAperturaTurno));
                    documento.add(new com.itextpdf.text.Paragraph("Cierre: " + new java.util.Date().toString()));
                    documento.add(new com.itextpdf.text.Paragraph("----------------------------------------"));
                    documento.add(new com.itextpdf.text.Paragraph("Base Inicial:        $ " + baseCaja));
                    documento.add(new com.itextpdf.text.Paragraph("Total Ventas:        $ " + totalVentasTurno));
                    documento.add(new com.itextpdf.text.Paragraph("----------------------------------------"));
                    documento.add(new com.itextpdf.text.Paragraph("TOTAL EN CAJA:       $ " + granTotalCaja));
                    documento.add(new com.itextpdf.text.Paragraph("========================================"));
                    documento.close();

                    // Abrimos el PDF automático
                    java.awt.Desktop.getDesktop().open(new java.io.File(nombreArchivo));
                } catch (Exception pdfEx) {
                    System.out.println("Error al crear PDF de Cierre: " + pdfEx.getMessage());
                }
                // --- FIN DEL PDF ---

                javax.swing.JOptionPane.showMessageDialog(this, "Turno cerrado exitosamente. El reporte ha sido generado.");
                
                // 3. Reiniciamos la seguridad de la pantalla
                baseCaja = 0;
                idTurnoActual = 0;
                horaAperturaTurno = "";
                
                btnAbrirCaja.setEnabled(true);   // Dejamos listo para el nuevo cajero
                jButton5.setEnabled(false);      // Apagamos botón cerrar
                jButton1.setEnabled(false);      // Bloqueamos mesas por seguridad
            }
            conexion.close();
        } catch (Exception e) {
            System.out.println("Error en Cierre Pro: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

