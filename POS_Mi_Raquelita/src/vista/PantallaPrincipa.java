    public void cargarGraficoVentas() {
    // 1. Creamos el contenedor de los datos
    org.jfree.data.category.DefaultCategoryDataset dataset = new org.jfree.data.category.DefaultCategoryDataset();

    try {
        // 2. Nos conectamos a la base de datos
        conexion.ConexionDB con = new conexion.ConexionDB();
        java.sql.Connection conexion = con.establecerConexion();
        java.sql.Statement st = conexion.createStatement();
        
        // 3. Consulta: Sumamos las cantidades vendidas por cada producto y sacamos los 5 mejores
        String sql = "SELECT producto, SUM(cantidad) as total_vendido FROM ventas GROUP BY producto ORDER BY total_vendido DESC LIMIT 5";
        java.sql.ResultSet rs = st.executeQuery(sql);

        // 4. Metemos los datos de MySQL al contenedor de la gráfica
        while (rs.next()) {
            String nombreProducto = rs.getString("producto");
            String nombreFormateado = nombreProducto.replace(" ", "\n");
            int cantidad = rs.getInt("total_vendido");
            dataset.addValue(cantidad, "Ventas", nombreFormateado);
        }
        conexion.close();
    } catch (Exception e) {
        System.out.println("Error al cargar datos para la gráfica: " + e.getMessage());
    }

    // 5. Dibujamos la gráfica de barras
    org.jfree.chart.JFreeChart grafica = org.jfree.chart.ChartFactory.createBarChart(
        "Top 5 Productos Más Vendidos", // Título de la gráfica
        "Productos",                    // Etiqueta del Eje X (Abajo)
        "Unidades Vendidas",            // Etiqueta del Eje Y (Lado)
        dataset,
        org.jfree.chart.plot.PlotOrientation.VERTICAL,
        false, true, false
    );
    
    org.jfree.chart.plot.CategoryPlot plot = grafica.getCategoryPlot();
    org.jfree.chart.axis.CategoryAxis ejeX = plot.getDomainAxis();
    ejeX.setCategoryLabelPositions(org.jfree.chart.axis.CategoryLabelPositions.UP_45);
    ejeX.setMaximumCategoryLabelLines(3);
    org.jfree.chart.axis.NumberAxis ejeY = (org.jfree.chart.axis.NumberAxis) plot.getRangeAxis();
    ejeY.setStandardTickUnits(org.jfree.chart.axis.NumberAxis.createIntegerTickUnits());
    
    // 1. Definimos una paleta de colores professional para cafetería
    java.awt.Color[] colores = {
        new java.awt.Color(139, 69, 19),  // ☕ Café Oscuro
        new java.awt.Color(205, 133, 63), // 🥐 Café con Leche / Dorado Pastelería
        new java.awt.Color(255, 165, 0),   // 🍊 Naranja Brillante (Jugos/Aguapanela)
        new java.awt.Color(255, 215, 0),   // ✨ Oro (Toque premium/Empanadas)
        new java.awt.Color(107, 142, 35)   // 🍵 Verde Oliva (Un toque distintivo y natural)
    };

    // 2. Creamos un renderizador de barras personalizado para asignar colores por ítem
    org.jfree.chart.renderer.category.BarRenderer renderer = new org.jfree.chart.renderer.category.BarRenderer() {
        @Override
        public java.awt.Paint getItemPaint(int row, int column) {
            // Asigna un color diferente a cada barra (columna) basándose en nuestra paleta
            // Usamos el operador % por si vendiéramos más de 5, para que los colores se repitan
            return colores[column % colores.length];
        }
    };

    // 3. Un toque final: Quitar los bordes negros de las barras para que se vean más limpias
    renderer.setDrawBarOutline(false);
    
    // 4. Aplicamos el nuevo renderizador colorido a la gráfica
    plot.setRenderer(renderer);
    

    // 6. Convertimos la gráfica en un Panel visual
    org.jfree.chart.ChartPanel panelGrafica = new org.jfree.chart.ChartPanel(grafica);
    // Le decimos que tome el tamaño exacto de tu jPanel1
    panelGrafica.setPreferredSize(new java.awt.Dimension(jPanel1.getWidth(), jPanel1.getHeight()));

    // 7. Acomodamos tu jPanel1 y le pegamos la gráfica adentro
    jPanel1.setLayout(new java.awt.BorderLayout());
    jPanel1.removeAll(); // Limpiamos por si había algo antes
    jPanel1.add(panelGrafica, java.awt.BorderLayout.CENTER);
    jPanel1.validate(); // Refrescamos la pantalla para que se vea
}
    
    public void cargarGraficoPastel() {
    // 1. Creamos el contenedor de datos circular
    org.jfree.data.general.DefaultPieDataset dataset = new org.jfree.data.general.DefaultPieDataset();

    try {
        // 2. Nos conectamos a la base de datos
        conexion.ConexionDB con = new conexion.ConexionDB();
        java.sql.Connection conexion = con.establecerConexion();
        java.sql.Statement st = conexion.createStatement();
        
        // 3. Consulta: Ahora no sumamos "cantidad", sino "subtotal" (el dinero)
        String sql = "SELECT producto, SUM(subtotal) as ingresos FROM ventas GROUP BY producto ORDER BY ingresos DESC LIMIT 5";
        java.sql.ResultSet rs = st.executeQuery(sql);

        // 4. Llenamos el pastel con los datos de ganancias
        while (rs.next()) {
            String nombreProducto = rs.getString("producto");
            String nombreFormateado = nombreProducto.replace(" ", "\n");
            double ingresos = rs.getDouble("ingresos");
            // Aquí le pasamos el nombre y la platica que generó
            dataset.setValue(nombreFormateado, ingresos); 
        }
        conexion.close();
    } catch (Exception e) {
        System.out.println("Error al cargar datos para el pastel: " + e.getMessage());
    }

    // 5. Dibujamos el gráfico de pastel
    org.jfree.chart.JFreeChart graficaPastel = org.jfree.chart.ChartFactory.createPieChart(
        "Top 5 Productos por Ingresos ($)", // Título
        dataset,                            // Los datos
        true,                               // Mostrar cuadro de colores (Leyenda)
        true,                               // Mostrar etiquetas al pasar el mouse
        false
    );

    // --- Toques de estética para que se vea moderno ---
    org.jfree.chart.plot.PiePlot plot = (org.jfree.chart.plot.PiePlot) graficaPastel.getPlot();
    plot.setLabelGenerator(null);
    plot.setBackgroundPaint(java.awt.Color.WHITE); // Fondo blanco limpio
    plot.setOutlineVisible(false); // Quitar el borde negro exterior
    // --------------------------------------------------

    // 6. Convertimos el gráfico en un Panel visual
    org.jfree.chart.ChartPanel panelGrafica = new org.jfree.chart.ChartPanel(graficaPastel);
    panelGrafica.setPreferredSize(new java.awt.Dimension(jPanel2.getWidth(), jPanel2.getHeight()));

    // 7. Lo agregamos a tu nuevo jPanel2
    jPanel2.setLayout(new java.awt.BorderLayout());
    jPanel2.removeAll(); 
    jPanel2.add(panelGrafica, java.awt.BorderLayout.CENTER);
    jPanel2.validate(); 
}

public void comprobarEstadoCaja() {
    try {
        conexion.ConexionDB con = new conexion.ConexionDB();
            java.sql.Connection conexion = con.establecerConexion();
            java.sql.Statement st = conexion.createStatement();

            // ---  1. ESTADO POR DEFECTO (Caja Cerrada)  ---
            // Asumimos que no hay turno. Apagamos ventas y encendemos apertura.
            btnAbrirCaja.setEnabled(true);   // Puedes abrir caja
            jButton1.setEnabled(false);      // NO puedes gestionar mesas
            jButton5.setEnabled(false);      // NO puedes cerrar un turno inexistente

            // 2. Buscamos si en la base de datos hay una caja ABIERTA
            String sql = "SELECT *, DATE(fecha_apertura) as solo_fecha FROM caja_diaria WHERE estado = 'ABIERTA' ORDER BY id DESC LIMIT 1";
            java.sql.ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                // ---  3. SI HAY CAJA ABIERTA, CAMBIAMOS LAS REGLAS  ---
                btnAbrirCaja.setEnabled(false); // Ya está abierta, no puedes abrir otra
                jButton5.setEnabled(true);      // Se habilita el botón de Cerrar Turno

                String fechaCajaTexto = rs.getString("solo_fecha");
                String fechaHoyTexto = new java.text.SimpleDateFormat("yyyy-MM-dd").format(new java.util.Date());

                if (!fechaCajaTexto.equals(fechaHoyTexto)) {
                    // CASO A: Es de un día anterior (Bloqueamos mesas por seguridad)
                    javax.swing.JOptionPane.showMessageDialog(this, 
                        "¡ATENCIÓN SEGURIDAD!\nHay una caja abierta del día anterior: " + fechaCajaTexto 
                        + "\nDebes cerrar ese turno antes de poder trabajar hoy.", 
                        "Cierre Pendiente Detectado", javax.swing.JOptionPane.WARNING_MESSAGE);
                    
                    jButton1.setEnabled(false); // Mesas siguen bloqueadas
                } else {
                    // CASO B: Es de hoy. ¡Todo en orden, a vender!
                    jButton1.setEnabled(true); // ¡SE HABILITAN LAS MESAS!
                    
                    // Guardamos los datos en la memoria
                    idTurnoActual = rs.getInt("id");
                    baseCaja = rs.getDouble("base_inicial");
                    horaAperturaTurno = rs.getString("fecha_apertura");
                }
            }
            conexion.close();
            
        } catch (Exception e) {
            System.out.println("Error en Auditoría Pro: " + e.getMessage());
        }
}

    

