    // Esto es lo que le enseña a la tabla a leer el cerebro ---
    private void cargarTabla() {
        javax.swing.table.DefaultTableModel modeloTabla = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        
        // 1. Limpiamos la tabla visual para que no se duplique lo viejo con lo nuevo
        modeloTabla.setRowCount(0);
        
        // 2. Un ciclo "For": Recorremos el cerebro y por cada producto que haya...
        for (modelo.Producto prod : miControlador.obtenerTodosLosProductos()) {
            
            // ...lo agregamos como una fila nueva en la tabla
            modeloTabla.addRow(new Object[]{
                prod.getId(), 
                prod.getNombre(), 
                prod.getCategoria(), 
                prod.getPrecio(), 
                prod.getCantidad()
            });
        }
    }
    

